// 根据六爻查找对应的卦
export function getHexagramByLines(lines: number[]): Hexagram | null {
  // 将六爻转换为二进制字符串（从下到上）
  const binary = lines.map((line) => (line === 1 ? "1" : "0")).join("");
  return hexagrams.find((h) => h.binary === binary) || null;
}
